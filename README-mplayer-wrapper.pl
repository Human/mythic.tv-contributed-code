Copy mplayer-wrapper.pl to /usr/local/bin/ on your media box.

Run "chmod a+x" on it to make it executable.

If using MythTV, go into your MythTV settings and replace all instances of the mplayer command with mplayer-wrapper.pl, making sure to remove the '--quiet' parameter, if used. All other parameters seem to be compatible with the wrapper.

Watch a video on your HDD, and you should get the best parameters for playing that video, based on polls of its codec.  Because it calls mplayer-resumer.pl, you also gain the benefits of using mplayer-resumer.pl.

