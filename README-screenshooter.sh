* Copy screenshooter.sh and shootscreens.sh to /usr/local/bin/ on your media box.
* Run "chmod a+x" on them to make them executable.
* If using MythTV, run the Video Manager, then back out of it.
* Run a command like this to process an individual video:

      screenshooter.sh -v /myth/video/HDTV/monkey.mpg

  or like this to process all videos known to the Video Manager:

      shootscreens.sh

